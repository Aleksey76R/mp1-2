#include <stdio.h>
#include <math.h>

int main()
{
    double x1, y1, r1, x2, y2, r2;

    printf("Введите координаты центра и радиуса первой окружности без запятой x1 y1 r1: ");
    scanf("%lf %lf %lf", &x1, &y1, &r1);

    printf("Введите координаты центра и радиуса второй окружности без запятой x2 y2 r2: ");
    scanf("%lf %lf %lf", &x2, &y2, &r2);

    double d = sqrt((x2 - x1) * (x2 - x1) + (y2 - y1) * (y2 - y1));

    if (d == 0 && r1 == r2)
    {
        printf("Окружности совпадают.\n");
    }
    else if (d > r1 + r2)
    {
        printf("Окружности не пересекаются (внешние).\n");
    }
    else if (d == r1 + r2)
    {
        printf("Окружности касаются внешним образом.\n");
    }
    else if (d < fabs(r1 - r2))
    {
        printf("Одна окружность внутри другой целиком.\n");
    }
    else if (d == fabs(r1 - r2))
    {
        printf("Окружности касаются внутренним образом.\n");
    }
    else
    {
        printf("Окружности пересекаются в двух точках.\n");
    }

    return 0;
}
